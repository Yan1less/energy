package com.yan1less.enegry.pojo;

public class eventInfo {
    private String event_type;
    private String warning_info;
    private Integer warning_number;

    public String getEvent_type() {
        return event_type;
    }

    public void setEvent_type(String event_type) {
        this.event_type = event_type;
    }

    public String getWarning_info() {
        return warning_info;
    }

    public void setWarning_info(String warning_info) {
        this.warning_info = warning_info;
    }

    public Integer getWarning_number() {
        return warning_number;
    }

    public void setWarning_number(Integer warning_number) {
        this.warning_number = warning_number;
    }
}
